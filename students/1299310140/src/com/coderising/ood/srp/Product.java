package com.coderising.ood.srp;

public class Product {
	
	private String id;
	
	private String desc;

	public Product(String id, String desc) {
		super();
		this.id = id;
		this.desc = desc;
	}

	public String getId() {
		return id;
	}

	public String getDesc() {
		return desc;
	}
	
	
}
